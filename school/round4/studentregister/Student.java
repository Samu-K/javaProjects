class Student {
    private String name;
    private String studnum;

    public Student(String name, String studentnumber)
    {
        this.name = name;
        this.studnum = studentnumber;
    }

    public String getName()
    {
        return this.name;
    }

    public String getStudentNumber()
    {
        return this.studnum;
    }
    
}